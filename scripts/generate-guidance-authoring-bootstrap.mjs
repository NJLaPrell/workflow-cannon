#!/usr/bin/env node
/**
 * Extracts the guidance authoring webview IIFE from GuidancePanel.ts, applies
 * ID_PREFIX + gpRootEl scoping for dashboard embedding, and emits
 * extensions/cursor-workflow-cannon/src/views/guidance/guidance-authoring-webview-bootstrap.ts
 *
 * Run from repo root: node scripts/generate-guidance-authoring-bootstrap.mjs
 */
import fs from "node:fs";
import path from "node:path";

const root = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const panelPath = path.join(
  root,
  "extensions/cursor-workflow-cannon/src/views/guidance/GuidancePanel.ts"
);
const outPath = path.join(
  root,
  "extensions/cursor-workflow-cannon/src/views/guidance/guidance-authoring-webview-bootstrap.ts"
);

const src = fs.readFileSync(panelPath, "utf8");
const startMark = "const bootstrap = `";
const endMark = "`;\n    return `<!DOCTYPE html>";
const i = src.indexOf(startMark);
const j = src.indexOf(endMark, i);
if (i < 0 || j < 0) {
  console.error("Could not locate bootstrap template literal in GuidancePanel.ts");
  process.exit(1);
}
let b = src.slice(i + startMark.length, j);

const OLD_HEAD =
  "(function(){var vscode=acquireVsCodeApi();function el(id){return document.getElementById(id);}";
const NEW_HEAD =
  '(function(){var ID_PREFIX=__WK_ID_PREFIX__;var gpRootEl=document.currentScript&&document.currentScript.parentElement||document.documentElement;function gpStripId(raw){raw=String(raw||"");return typeof ID_PREFIX==="string"&&ID_PREFIX.length&&raw.indexOf(ID_PREFIX)===0?raw.slice(ID_PREFIX.length):raw;}var vscode=window.__wfcVscode||(window.__wfcVscode=acquireVsCodeApi());function el(id){if(id==="wc-drawer-host"||id==="wc-drawer-validation")return document.getElementById(id);return document.getElementById(ID_PREFIX+id);}';

if (!b.startsWith(OLD_HEAD)) {
  console.error("Bootstrap head literal changed; update OLD_HEAD in generate-guidance-authoring-bootstrap.mjs");
  process.exit(1);
}
b = NEW_HEAD + b.slice(OLD_HEAD.length);

const reps = [
  ["document.querySelectorAll('[data-gp-tab]')", "gpRootEl.querySelectorAll('[data-gp-tab]')"],
  ["document.querySelectorAll('[data-gp-panel]')", "gpRootEl.querySelectorAll('[data-gp-panel]')"],
  ["document.querySelectorAll('[data-gp-'+kind+'-row]')", "gpRootEl.querySelectorAll('[data-gp-'+kind+'-row]')"],
  ["document.querySelectorAll('[data-gp-activation-artifact]')", "gpRootEl.querySelectorAll('[data-gp-activation-artifact]')"],
  ["document.querySelector('[data-gp-activation-artifact]:checked')", "gpRootEl.querySelector('[data-gp-activation-artifact]:checked')"],
  ["document.querySelectorAll('[data-gp-activation-bulk]')", "gpRootEl.querySelectorAll('[data-gp-activation-bulk]')"],
  [
    "document.querySelectorAll('[data-gp-activation-bulk]:checked')",
    "gpRootEl.querySelectorAll('[data-gp-activation-bulk]:checked')"
  ],
  [
    "document.body.addEventListener('input',function(ev){var id=ev.target&&ev.target.id;",
    "gpRootEl.addEventListener('input',function(ev){var id=gpStripId(ev.target&&ev.target.id);"
  ],
  [
    "document.body.addEventListener('change',function(ev){var id=ev.target&&ev.target.id;",
    "gpRootEl.addEventListener('change',function(ev){var id=gpStripId(ev.target&&ev.target.id);"
  ],
  ["document.body.addEventListener('click',function(ev){var t=ev.target;", "gpRootEl.addEventListener('click',function(ev){var t=ev.target;"],
  [
    'Preview evidence<textarea id="gp-preview-evidence" rows="7" readonly>\'+esc',
    "Preview evidence<textarea id=\"'+ID_PREFIX+'gp-preview-evidence\" rows=\"7\" readonly>'+esc"
  ],
  [
    "if(m&&m.type==='wcDrawerValidation'&&typeof m.message==='string'){var vv=document.getElementById('wc-drawer-validation');",
    "if(m&&m.type==='wcDrawerValidation'&&typeof m.message==='string'){var vv=el('wc-drawer-validation');"
  ]
];
for (const [from, to] of reps) {
  const n = b.split(from).length - 1;
  if (n === 0) {
    console.error("Missing expected substring:", from.slice(0, 80));
    process.exit(1);
  }
  b = b.split(from).join(to);
}

const marker = "__WK_ID_PREFIX__";
if (!b.includes(marker)) {
  console.error("Expected marker not present after transform");
  process.exit(1);
}

const escapedBody = JSON.stringify(b);
const file = `/* eslint-disable @typescript-eslint/no-unused-expressions */
/**
 * Guidance authoring panel client bootstrap (shared by Guidance webview + Dashboard CAE tab).
 * Generated by \`node scripts/generate-guidance-authoring-bootstrap.mjs\` — edit the source IIFE in
 * GuidancePanel.ts (or this script's transforms), then re-run the generator.
 */
const GUIDANCE_AUTHORING_WEBVIEW_BOOTSTRAP_TEMPLATE = ${escapedBody};

export function buildGuidanceAuthoringWebviewBootstrap(idPrefix: string): string {
  return GUIDANCE_AUTHORING_WEBVIEW_BOOTSTRAP_TEMPLATE.split(${JSON.stringify(marker)}).join(
    JSON.stringify(idPrefix)
  );
}
`;

fs.writeFileSync(outPath, file, "utf8");
console.log("Wrote", outPath);
